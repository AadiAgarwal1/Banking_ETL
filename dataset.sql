-- DROP database banking_system;
CREATE DATABASE IF NOT EXISTS banking_system;
USE banking_system;
SELECT * FROM branches;
SELECT * FROM Customers;
-- 1. Customers
CREATE TABLE  Customers (
    customer_id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(100),
    email VARCHAR(100),
    phone VARCHAR(15),
    address TEXT,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);

-- 2. Accounts
CREATE TABLE Accounts (
    account_id INT PRIMARY KEY AUTO_INCREMENT,
    customer_id INT,
    account_type ENUM('Savings', 'Current', 'Fixed Deposit'),
    balance DECIMAL(15,2),
    opened_on DATE,
    FOREIGN KEY (customer_id) REFERENCES Customers(customer_id)
);

-- 3. Branches
CREATE TABLE Branches (
    branch_id INT PRIMARY KEY AUTO_INCREMENT,
    branch_name VARCHAR(100),
    location VARCHAR(100)
);

-- 4. Employees
CREATE TABLE Employees (
    employee_id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(100),
    designation VARCHAR(50),
    branch_id INT,
    email VARCHAR(100),
    FOREIGN KEY (branch_id) REFERENCES Branches(branch_id)
);
SELECT * FROM Employees;
-- 5. Transactions
CREATE TABLE Transactions (
    transaction_id INT PRIMARY KEY AUTO_INCREMENT,
    account_id INT,
    transaction_type ENUM('Credit', 'Debit'),
    amount DECIMAL(12,2),
    transaction_date DATETIME,
    FOREIGN KEY (account_id) REFERENCES Accounts(account_id)
);

-- 6. Loans
CREATE TABLE Loans (
    loan_id INT PRIMARY KEY AUTO_INCREMENT,
    customer_id INT,
    loan_type VARCHAR(50),
    amount DECIMAL(15,2),
    status ENUM('Approved', 'Pending', 'Rejected'),
    start_date DATE,
    end_date DATE,
    FOREIGN KEY (customer_id) REFERENCES Customers(customer_id)
);

-- 7. Cards
CREATE TABLE Cards (
    card_id INT PRIMARY KEY AUTO_INCREMENT,
    customer_id INT,
    card_type ENUM('Debit', 'Credit'),
    card_number VARCHAR(20),
    expiry_date DATE,
    FOREIGN KEY (customer_id) REFERENCES Customers(customer_id)
);

-- 8. LoginCredentials
CREATE TABLE LoginCredentials (
    login_id INT PRIMARY KEY AUTO_INCREMENT,
    customer_id INT,
    username VARCHAR(50),
    password_hash VARCHAR(255),
    last_login DATETIME,
    FOREIGN KEY (customer_id) REFERENCES Customers(customer_id)
);

-- 9. Services
CREATE TABLE Services (
    service_id INT PRIMARY KEY AUTO_INCREMENT,
    service_name VARCHAR(100),
    description TEXT
);

-- 10. CustomerServices
CREATE TABLE CustomerServices (
    customer_id INT,
    service_id INT,
    subscribed_on DATE,
    PRIMARY KEY (customer_id, service_id),
    FOREIGN KEY (customer_id) REFERENCES Customers(customer_id),
    FOREIGN KEY (service_id) REFERENCES Services(service_id)
);

ALTER TABLE Loans ADD COLUMN branch_id INT;
ALTER TABLE Loans ADD FOREIGN KEY (branch_id) REFERENCES Branches(branch_id);

ALTER TABLE Accounts ADD COLUMN branch_id INT;
